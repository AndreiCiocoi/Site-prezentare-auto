 <img src="imagini/etichetaue.jpg"></a>
 <p id="border" style='color:white;font-size:120%;'>Eticheta EU ne arata eficienta, franarea pe carosabil umed si nivelul de zgomot</p>
 <h3 class="h4" style='text-align:left;'><b>2. Echilibrarea rotilor la fiecare inceput de sezon</b></h3>
 <p style='font-size:120%;text-indent: 50px;'>Nu conteaza daca avem un set suplimentar de vara fata de cel de iarna, cu jante si 
 anvelope separate, sau daca avem doar anvelopele pe care le montam pe acelasi set de jante. 
 <b>Atunci cand facem schimbul, fie ca este de vara sau de iarna, specialistii ne recomanda sa facem o
 echilibrare a celor 4 roti.</b> Pentru ca sunt sanse ca, de-a lungul folosintei, ori plumbii sa fi cazut, 
 ori jantele sa se fi strambat sau pneurile sa aiba galme sau deformari din pricina gropilor. 
 Asa ca este recomandat sa echilibram si daca montam doar anvelopele, dar si daca montam setul complet 
 jante + anvelope, ca sa ne asiguram ca nu o sa avem probleme in sezonul cald care urmeaza.</p>
 <h3 class="h4"style='text-align:left;'><b>3. Presiunea perfecta in anvelope</b></h3>
 <p style='font-size:120%;text-indent: 50px;'>Multi soferi din Romania omit sa verifice presiunea din anvelope si circula fara sa umble la roti
 cu luni sau chiar ani. Mereu trebuie sa ne asiguram ca avem in pneuri presiunea recomandata de 
 producator sau inscrisa pe automobilul nostru, in zona stalpului dintre portiere. <b>Daca vom circula 
 cu presiune mai mica vom uza anvelope neuniform si aceasta se va incalzi mai repede, iar daca avem 
 presiune prea mare, la viteza csrecuta, din cauza ca aerul se dilata, riscam sa avem parte de o explozie.</b>
 Asadar, nu strica sa ne uitam cel putin o data pe luna la presiunea din rotile noastre.</p>
