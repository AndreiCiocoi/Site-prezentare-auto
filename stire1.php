 <h3 class="h4"style='text-align:left;'><b>6. Mai putin de 1,6 mm profil ne poate costa viata</b></h3>
 <p style='font-size:120%;text-indent: 50px;'><b>Conform legislatiei in vigoare, Registrul Auto Roman spune ca o anvelopa nu are voie sa fie 
 utilizata daca are profil mai mic de 1,6 mm.</b> Noi credem ca este putin si ca profilul trebuie sa fie
 mai mare pentru franare optima, inclusiv in cazul pneurilor de iarna care daca au mai putin de 5 mm 
 in carne, nu mai sunt anvelope de iarna! Daca insa nu ne intereseaza siguranta noastra si a pasagerilor,
 e bine sa respectam legea si sa ne asiguram ca avem mereu anvelope cu profil de peste 1,6 mm, in caz 
 contrar riscam amenzi usturatoare. <b>Mai mult, daca suntem implicati intr-un accident, indiferent ca suntem
 pagubit sau vinovat, riscam ca societatea de asigurari sa nu ne despagubeasca din cauza anvelopelor uzate!</b></p>
 <h3 class="h4"style='text-align:left;'><b>7. Geometria, recomandata cel putin o data pe an</b></h3>
 <p style='font-size:120%;text-indent: 50px;'>Specialistii ne recomanda sa facem geometria rotilor cel putin o data pe an, de preferat la sfarsit 
 de iarna, atunci cand ne montam pneurile de vara. Pentru ca din cauza gropilor, din cauza anvelopelor 
 dezechilibrate, din cauza bordurilor si a sinelor de tramvai, sistemul de directie al masinii are de 
 suferit. <b>Exista riscul ca unele suruburi sa se slabeasca, unele piese sa se indoaie si unele conexiuni 
 din cauciuc, bucse sau pivoti, sa cedeze.</b> Aceste lucruri apar in general la sfarsit de iarna, asa ca 
 odata cu montarea noului set de vara, ca sa ne asiguram ca masina este in regula si ca nu va uza pneurile
 neuniform, e bine sa facem o geometrie.</p>
