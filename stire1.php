 <h3 class="h4"style='text-align:left;'><b>8. Verificarea lichidelor si a bateriei auto</b></h3>
 <p style='font-size:120%;text-indent: 50px;'>Tot un sfat la inceputul sezonului cald, ca sa evitam problemele din concediu, este sa ne uitam la 
 nivelul si calitatea lichidelor de sub capota motorului. <b>Ideal ar fi sa mergem intr-un service 
 specializat care este dotat cu aparate de masura.</b> Pentru ca trebuie sa verificam lichidul de frana,
 daca este la nivel si daca mai este bun, antigelul, daca este la nivel si daca mai are eficienta, 
 lichidul de servo si chiar uleiul motorului. <b>In plus, acumulatorul masinii, dupa o iarna grea, are 
 toate sansele sa cedeze. Un test cu un aparat dedicat, care ne spune daca bateria noastra inmagazineaza
 corect curent, este ideal, ca sa nu ne trezim pe autostrada, cand mergem spre litoral, ca nu mai avem
 curent.</b></p>
