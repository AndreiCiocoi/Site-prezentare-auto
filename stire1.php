 <p id="border"style='color:white;font-size:120%;'>Putem sa verificam presiunea si singuri, in orice benzinarie</p>
 <h3 class="h4"style='text-align:left;'><b>4. Cauciucurile all-season, un compromis nedorit</b></h3>
 <p style='font-size:120%;text-indent: 50px;'>Anvelopele dedicate folosirii anului intreg, numite mixte sau all-season, sunt bune la tot
 si bune la nimic. Adica au un profil jumatate de iarna, jumatate de vara, ceea ce inseamna ca nu se
 vor comporta excelent nici vara, nici iarna. In plus, materialul din care sunt facute este cel folosit
 la anvelopele de iarna, ceea ce inseamna ca acestea se vor uza mai repede vara. Si cum in Romania zilele
 calduroase sunt mai mult decat cele geroase, anvelopele all-season nu prea sunt de dorit.<b> Acum au aparut
 si anvelope de vara, omologate si pentru iarna, care sunt mult mai bune, insa ideal este sa avem cate un
 set pentru fiecare sezon daca vrem sa ne asiguram de franare, longevitate si aderenta optime, fie ca este
 canicula, fie ca este polei si ger.</b></p>
 <h3 class="h4"style='text-align:left;'><b>5. Anvelopa dedicata sezonului in care suntem</b></h3>
 <p style='font-size:120%;text-indent: 50px;'>Producatorii de pneuri nu au facut degeaba doua tipuri, in functie de sezon, adica pneuri de vara
 si pneuri de iarna. Ci pentru ca au vrut sa ofere siguranta maxima soferilor din toata lumea.
 <b>Asadar, indiferent ca ninge sau nu, iarna este considerat sezonul in care temperatura medie a unei 
 saptamani scade sub 7 grade Celsius.</b> Cand termometrul incepe sa ne arate mai putin, e timpul sa trecem 
 la anvelopele de iarna. Acelasi lucru se intampla prin lunile martie, aprilie: cand vedem ca temperatura
 zilei este de peste 7 grade Celsius, e timpul sa trecem la setul de vara. <b>Orice tentativa de a 'pacali'
 acest sistem eficient poate duce la accidente grave.</b></p>
